public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("1. Разные переменные");
        // цифра
        byte number = 5;
        System.out.println("Переменная number равна: " + number);

        // сумма чисел
        short sumNumbers = 12 + 13;
        System.out.println("Сумма чисел равна: " + sumNumbers);
        
        // произведение цифр
        int productDigits = 3 * 9;
        System.out.println("Произведение цифр равно: " + productDigits);

        // максимальное число
        int maxNumber = 2147483647;
        System.out.println("Максимальная величина целого числа типа int равна: " + maxNumber);

        // количество десятков
        int quantityTens = 10;
        System.out.println("Число имеет следующее количество десятков: " + quantityTens);

        // вес собаки
        int dogWeight = 16;
        System.out.println("Моя собака весит: " + dogWeight + " кг");

        // исходное число
        int initialNumber = 321;
        System.out.println("Исходное число равно: " + initialNumber);

        // процент по вкладу
        byte interestOnDeposit = 11;
        System.out.println("Процент по вкладу составляет " + interestOnDeposit + "% годовых");

        // символ &
        char ampersand = '&';
        System.out.println("Амперсанд выглядит так: " + ampersand);

        // код ошибки
        int errorCode = 500;
        System.out.println("При отправке запроса на сайт я получил ошибку с кодом " + errorCode);

        // тип сообщения
        String messageType = "ERROR";
        System.out.println("От сервера вернулось сообщение типа " + messageType);

        // число нулей
        byte quantityZeros = 6;
        System.out.println("Размер моей зарплаты содержит " + quantityZeros + " нулей");

        // уникальное число
        int uniqueNumber = 369;
        System.out.println("Моё любимое уникальное число: " + uniqueNumber);

        // случайное число
        int randomNumber = 777;
        System.out.println("Вчера я играл в игровые автоматы и мне выпало число " + randomNumber);

        // математическое выражение
        String mathExpression = "a * b";
        System.out.println("Для вычисления площади прямоугольника воспользуемся формулой: "
                + mathExpression);

        // выбор чего-либо
        boolean choice = false;
        System.out.println("Пойду ли я завтра на работу? " + choice);

        // счёт
        String score = "4:0";
        System.out.println("Вчера моя команда победила со счётом " + score);

        // максимальную длину строки
        byte maxLineLength = 25;
        System.out.println("Поле Имя может содержать не больше " + maxLineLength + " символов");

        // пункт меню
        String menuItem = "Network";
        System.out.println("Разработанный виджет меню содержит следующий пункт: " + menuItem);

        // стоимость кофе на вынос
        byte costCoffeeToGo = 110;
        System.out.println("В этом заведение кофе с собой стоит " + costCoffeeToGo + " рублей");

        // дату начала
        String startDate = "01.06.2024";
        System.out.println(startDate + " начнётся лето в этом году!" );

        // окончание диапазона
        byte endRange = 76;
        System.out.println("Этим числом заканчивается диапазон целых чисел: " + endRange);

        // полное имя работника месяца
        String fullNameEmployeeMonth = "Александр Сергеевич Пушкин";
        System.out.println("В этом месяце лучший работник - " + fullNameEmployeeMonth);

        // заголовок электронной книги
        String ebookTitle = "Брось всё... и читай!";
        System.out.println("Хороший заголовок: " + ebookTitle);

        // размер
        byte size = 48;
        System.out.println("Я ношу штаны " + size + " размера");

        // вместимость
        int capacity = 600;
        System.out.println("В мою ванну поместится " + capacity + " литров воды");

        // счётчик
        int counter = 1456;
        System.out.println("После сложения всех значений получилось следующее число: " + counter);

        // путь до файла
        String pathToFile = "D:\\AllJava\\StartJava";
        System.out.println("Мой файл с паролями находится по адресу: " + pathToFile);

        // количество чисел в каждой строке
        int quantityNumbersInEachRows = 8923;
        System.out.println("На этой странице " + quantityNumbersInEachRows + " символа\n");

        System.out.println("2. Boolean-переменные");

        // результат сравнения сотен
        boolean isHundredEqual = true;
        System.out.println("Сотни равны? - " + isHundredEqual);

        // информация о работе компьютера (вкл/выкл)
        boolean isPcOn = true;
        System.out.println("Ты включил компьютер? - " + isPcOn);

        // информация о наличии равных цифр
        boolean isDigitsEqual = false;
        System.out.println("В этом массиве есть одинаковые цифры? - " + isDigitsEqual);

        // информация о создании чего либо
        boolean isCreated = true;
        System.out.println("Строка создалась? - " + isCreated);

        // что-либо пустое
        boolean isEmpty = false;
        System.out.println("Ты заполнил поле Имя? - " + isEmpty);

        // что-либо активное
        boolean isActive = true;
        System.out.println("Ты можешь нажать на эту кнопку? - " + isActive);

        // информация о том, является ли объект новым
        boolean isNew = true;
        System.out.println("Этот объект был создан сегодня? - " + isNew);

        // информация о том, действительная ли электронная почта или нет
        boolean isEmailValid = false;
        System.out.println("Ты пользуешься этой почтой? - " + isEmailValid);

        // информация о наличии уникальных строк в таблице
        boolean isStringUnique = true;
        System.out.println("Эта строка содержится в тексте? - " + isStringUnique);

        System.out.println("\n3. Аббревиатуры");

        // старый universally unique identifier
        String oldUuid = "4896c91b-9e61-3129-87b6-8aa299028058";
        System.out.println("У этого объекта идентификатор " + oldUuid);

        // производитель оперативной памяти
        String ramManufacturer = "Patriot";
        System.out.println("Лучший бюджетный производитель оперативной памяти - " +
                ramManufacturer);

        // ёмкость жесткого диска
        String hddCapacity = "1TB";
        System.out.println("На моем жёстком диске " + hddCapacity);

        // протокол передачи гипертекста
        String http = "Hypertext Transfer Protocol";
        System.out.println("Для передачи гипертекста используется " + http);

        // сокращенный uniform resource locator
        String shortUrl = "google.ru";
        System.out.println("Перейди по этому адресу: " + shortUrl);

        // новый идентификатор клиента
        String newClientId = "2345123";
        System.out.println("Я присвоил клиенту новый идентификатор: " + newClientId);

        // кодировка american standard code for information interchange
        String ascii = "American standart code for information interchange";
        System.out.println("В фильме Марсианин слышал про " + ascii);
    }
}